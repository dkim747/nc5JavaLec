public class _2023_03_14_01_basic {

	public static void main(String[] args) {
		
		int num = 10;
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(num--);
		System.out.println(--num);
		System.out.println(num--);
		System.out.println(--num);
		
		int a = 10;
		int b = 20;
		System.out.println(a<b);
		System.out.println(a>b);
	}

}
